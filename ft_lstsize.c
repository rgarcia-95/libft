#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (lst != '\0')
	{
		while (lst != NULL)
		{
			i++;
			lst = lst->next;
		}
		return (i);
	}
	return (0);
}
