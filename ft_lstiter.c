#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	if (lst != '\0')
	{
		aux = lst;
		while (aux != '\0')
		{
			f(aux->content);
			aux = aux->next;
		}
	}
}
