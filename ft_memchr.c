#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)s;
	while (i < n)
	{
		if (a[i] == c)
			return ((void *)a + i);
		i++;
	}
	return (0);
}
